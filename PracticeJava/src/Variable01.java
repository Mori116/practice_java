/* 変数 */
public class Variable01 {

    public static void main(String[] args) {
        int num1; /* 変数の宣言 → 型 変数名; */
        String str1;
        num1 = 10; /* 変数宣言後、代入する */
        str1 = "Hello World!";
        System.out.println(num1);
        System.out.println(str1);

        num1 = 20; /* 再代入 */
        System.out.println(num1);

        int num2 = 30; /* 変数宣言と代入同時に記述可能 */
        System.out.println(num2);

    }

}
